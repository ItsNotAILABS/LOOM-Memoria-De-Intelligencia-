// WIRE ALL SYNAPSES - CONNECT EVERYTHING TO EVERYTHING
// ═══════════════════════════════════════════════════════════════════════════════

function wireAllSynapses(mesh: UniversalMesh): void {
    const allNodes = Array.from(mesh.nodeMap.values());
    
    // Connect every node to every other node in same layer (internal mesh)
    for (let layer = MeshLayer.QUANTUM; layer <= MeshLayer.INFINITE; layer++) {
        const layerNodes = mesh.nodesByLayer.get(layer) || [];
        
        for (let i = 0; i < layerNodes.length; i++) {
            for (let j = i + 1; j < layerNodes.length; j++) {
                // Bidirectional excitatory synapse within layer
                const synapse1 = createSynapse(
                    layerNodes[i].nodeId,
                    layerNodes[j].nodeId,
                    SynapseType.EXCITATORY
                );
                const synapse2 = createSynapse(
                    layerNodes[j].nodeId,
                    layerNodes[i].nodeId,
                    SynapseType.EXCITATORY
                );
                
                mesh.synapses.push(synapse1, synapse2);
                mesh.synapseMap.set(synapse1.synapseId, synapse1);
                mesh.synapseMap.set(synapse2.synapseId, synapse2);
                mesh.totalSynapses += 2;
            }
        }
    }
    
    // Connect adjacent layers (modulatory connections)
    for (let layer = MeshLayer.QUANTUM; layer < MeshLayer.INFINITE; layer++) {
        const currentNodes = mesh.nodesByLayer.get(layer) || [];
        const nextNodes = mesh.nodesByLayer.get(layer + 1) || [];
        
        for (const currentNode of currentNodes) {
            for (const nextNode of nextNodes) {
                // Modulatory synapse from lower to higher layer
                const synapse = createSynapse(
                    currentNode.nodeId,
                    nextNode.nodeId,
                    SynapseType.MODULATORY
                );
                mesh.synapses.push(synapse);
                mesh.synapseMap.set(synapse.synapseId, synapse);
                mesh.totalSynapses++;
                
                // Inhibitory synapse from higher to lower (feedback)
                const feedback = createSynapse(
                    nextNode.nodeId,
                    currentNode.nodeId,
                    SynapseType.INHIBITORY
                );
                mesh.synapses.push(feedback);
                mesh.synapseMap.set(feedback.synapseId, feedback);
                mesh.totalSynapses++;
            }
        }
    }
    
    // Connect twins to their originals (quantum entanglement)
    for (const node of allNodes) {
        if (node.isTwin && node.twinOf) {
            const originalNode = mesh.nodeMap.get(node.twinOf);
            if (originalNode) {
                const quantumSynapse = createSynapse(
                    originalNode.nodeId,
                    node.nodeId,
                    SynapseType.QUANTUM
                );
                mesh.synapses.push(quantumSynapse);
                mesh.synapseMap.set(quantumSynapse.synapseId, quantumSynapse);
                mesh.totalSynapses++;
            }
        }
        
        // Connect inverses (reverberant connection)
        if (node.isInverse && node.inverseOf) {
            const originalNode = mesh.nodeMap.get(node.inverseOf);
            if (originalNode) {
                const reverberantSynapse = createSynapse(
                    originalNode.nodeId,
                    node.nodeId,
                    SynapseType.REVERBERANT
                );
                mesh.synapses.push(reverberantSynapse);
                mesh.synapseMap.set(reverberantSynapse.synapseId, reverberantSynapse);
                mesh.totalSynapses++;
            }
        }
    }
}

