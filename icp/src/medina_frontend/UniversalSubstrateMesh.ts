// MESH TICK - PROCESS ONE HEARTBEAT
// ═══════════════════════════════════════════════════════════════════════════════

function meshTick(mesh: UniversalMesh): UniversalMesh {
    const now = Date.now();
    
    // Update phases for all nodes (Kuramoto oscillation)
    for (const [_, nodes] of mesh.nodesByLayer) {
        for (const node of nodes) {
            node.phase = (node.phase + (TAU * node.frequency / 1000)) % TAU;
        }
    }
    
    // Calculate global coherence (Kuramoto order parameter)
    let cosSum = 0;
    let sinSum = 0;
    let nodeCount = 0;
    
    for (const [_, nodes] of mesh.nodesByLayer) {
        for (const node of nodes) {
            cosSum += Math.cos(node.phase);
            sinSum += Math.sin(node.phase);
            nodeCount++;
        }
    }
    
    if (nodeCount > 0) {
        mesh.globalCoherence = Math.sqrt(cosSum * cosSum + sinSum * sinSum) / nodeCount;
    }
    
    mesh.lastTick = now;
    mesh.tickCount++;
    
    return mesh;
}

