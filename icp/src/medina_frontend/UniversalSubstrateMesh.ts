function phiHash(input: string): string {
    let h1 = 0x4D454449;  // "MEDI"
    let h2 = 0x4E415F50;  // "NA_P"
    
    for (let i = 0; i < input.length; i++) {
        const char = input.charCodeAt(i);
        h1 = ((h1 << 5) + h1) ^ char;
        h2 = (h2 * 33 + char) >>> 0;
        
        // Phi mixing
        const phiMix = Math.floor(h1 * PHI) >>> 0;
        h1 = (h1 ^ phiMix) >>> 0;
    }
    
    return `0x${h1.toString(16).padStart(8, '0')}_${h2.toString(16).padStart(8, '0')}`;
}

function animaHash(state: { coherence: number; beat: number; doctrineHash: string }): string {
    const combined = `${state.coherence.toFixed(10)}_${state.beat}_${state.doctrineHash}`;
    return phiHash(combined);
}

