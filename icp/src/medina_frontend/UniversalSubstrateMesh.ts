// HEBBIAN LEARNING - NEURONS THAT FIRE TOGETHER WIRE TOGETHER
// ═══════════════════════════════════════════════════════════════════════════════

function hebbianUpdate(
    synapse: Synapse,
    preActivation: number,
    postActivation: number,
    learningRate: number,
    coherence: number
): { synapse: Synapse; deltaWeight: number } {
    // Hebbian rule: Δw = η × pre × post × coherence
    const deltaWeight = learningRate * preActivation * postActivation * coherence;
    
    // Apply plasticity constraint
    const maxDelta = synapse.plasticity * synapse.baseWeight;
    const clampedDelta = Math.max(-maxDelta, Math.min(maxDelta, deltaWeight));
    
    const newWeight = Math.max(0, synapse.weight + clampedDelta);
    
    return {
        synapse: { ...synapse, weight: newWeight },
        deltaWeight: clampedDelta
    };
}

